package org.java2019;

public class For_Each_Loop {

  public static void main(String[] args) {

    String [] cars={"volvo", "benz", "honda", "audi", "bmw", "peugeot"};

    for(String i: cars){
      System.out.println(i);
    }



    int [] numbers={1,5,6,8,00,22,45,45};
    for (int b : numbers){
      System.out.println(b);
    }

  }

}
