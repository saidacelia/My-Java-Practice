package org.java2019;

public class While_DoWhile {

  public static void main(String[] args) {


    int a=4;
    while(a<5){
      System.out.println(a+ " a is less than 5");
      a++;
    }


    int b=0;
    do {
      System.out.println(b+ "  it's a do while loop");
      b++;
    }
    while(b<3);



  }

}
